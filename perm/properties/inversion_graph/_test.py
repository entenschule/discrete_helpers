from perm import Perm


def test():

    assert Perm([1, 0, 3, 2]).inversion_graph == {
        (0, 1): [(0, 1)],
        (2, 3): [(2, 3)]
    }

    assert Perm([3, 2, 1, 0]).inversion_graph == {
        (0, 1, 2, 3): [(0, 1), (0, 2), (0, 3), (1, 2), (1, 3), (2, 3)]
    }

    assert Perm([2, 1, 0, 6, 7, 5, 4, 3]).inversion_graph == {
        (0, 1, 2): [(0, 1), (0, 2), (1, 2)],
        (3, 4, 5, 6, 7): [(3, 5), (3, 6), (3, 7), (5, 4), (5, 6), (5, 7), (6, 4), (6, 7), (7, 4)]
    }

    assert Perm([3, 0, 2, 1, 6, 4, 7, 5]).inversion_graph == {
        (0, 1, 2, 3): [(0, 1), (0, 2), (0, 3), (2, 3)],
        (4, 5, 6, 7): [(4, 5), (4, 7), (7, 6)]
    }
